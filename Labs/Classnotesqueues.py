'''The Queue ADT
First in First out
Operations-

Queue() - creates a new empty Queue
Q.is_empty - return True if Q is empty
len(Q) - returns number of elements urrently in Q
Q.enqueue(elem) - adds an element to the Queue
Q.dequeue() - removes and returns the element at the front of the Queue,
              or raises an exception if the queue is empty
Q.front() - returns the element at the front of the queue without popping it'''


